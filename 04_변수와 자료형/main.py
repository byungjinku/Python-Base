# 리터럴 : 코드에 작성하는 값들을 의미
print("정수 :", 100)
print("실수 :", 11.11)
print("문자열 :", "안녕하세요")
print("문자열 :", '안녕하세요')
print("논리값 :", True, False)
print("없다는 의미 값 :", None)
print("복소수 :", 10 + 3j)

# type 함수 : 값의 형태를 확인할 수 있는 함수
print(type(100))
print(type(11.11))
print(type('안녕하세요'))
print(type(True))

# 변수
# 변수에 값을 저장한다.
# 만약 변수가 한번도 사용한적이 없는 변수라면
# 여기에서 변수가 새롭게 만들어진다.
a1 = 100
print("a1 :", a1)

# 변수를 생성할 때 변수에 저장할 값이 없다면
# None을 사용한다.
a2 = None
print("a2 :", a2)









